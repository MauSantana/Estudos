
package br.com.bandtec.lista02;


public class NumerosPares {
    public static void main(String[] args) {
        System.out.println("Numeros pares de 0 a 40");
        
        Integer contador = 0;
        
        while (contador <= 40) {            
            System.out.println(contador);
            contador+=2;
        }
    }
}
